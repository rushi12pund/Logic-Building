package com.example.demo.string;

public class RevString3 {

	public static void main(String[] args) {
		
          String Input = "Hello" ;
          
          for(int i=Input.length()-1;i>=0;i--) {
        	  
        	  char s = Input.charAt(i);
        	  
        	  System.out.println(s);
          }
		
          
		
	}

}
